package org.olat.system.support.mail.impl;

import static org.junit.Assert.assertFalse;

import org.junit.Test;
import org.junit.runner.RunWith;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.mail.javamail.JavaMailSenderImpl;
import org.springframework.test.context.ContextConfiguration;
import org.springframework.test.context.junit4.AbstractJUnit4SpringContextTests;
import org.springframework.test.context.junit4.SpringJUnit4ClassRunner;

/**
 * 
 * @author Christian Guretzki
 */
@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(locations = { "classpath:org/olat/system/support/mail/impl/_spring/mailContext.xml" })
public class MailServiceConfigITCaseNew extends AbstractJUnit4SpringContextTests {

    @Autowired
    MailServiceImpl mailService;

    @Test
    public void checkIsMailServiceDummyImpl() {
        assertFalse("Wrong MailSender configuration, only dummy-service without sending mail is allowed as default config",
                mailService.mailSender instanceof JavaMailSenderImpl);
    }

}
